// Helpers
function ensureCategoryOrder(order) {
  const list = Array.isArray(order) ? order.filter(Boolean) : [];
  if (list.length === 0) return [...DEFAULT_CATEGORY_ORDER];
  return Array.from(new Set(list));
}
function createEmptyCategoryMap(order = DEFAULT_CATEGORY_ORDER) {
  return order.reduce((acc, id) => { acc[id] = []; return acc; }, {});
}
function createLabelMap(order, baseLabels = {}) {
  const labels = {};
  order.forEach(id => {
    const trimmed = (baseLabels[id] || '').toString().trim();
    labels[id] = trimmed || DEFAULT_CATEGORY_LABEL_MAP[id] || `${DEFAULT_SUBGROUP_LABEL} ${id}`;
  });
  return labels;
}
function toKhmerOrdinal(position) {
  const numeric = Number(position ?? 0);
  if (Number.isNaN(numeric)) return (position ?? '').toString();
  const normalized = Math.max(0, Math.floor(Math.abs(numeric)));
  const digits = String(normalized).split('').map(ch => KHMER_DIGITS[(ch.charCodeAt(0) - 48)] ?? ch).join('');
  return digits || KHMER_DIGITS[0];
}
function buildGroupName(position, contextLabel) {
  const cleanLabel = (contextLabel || '').toString().trim();
  const ordinal = toKhmerOrdinal(position);
  return cleanLabel ? `ក្រុម${cleanLabel}ទី${ordinal}` : `ក្រុមទី${ordinal}`;
}
function normalizeGroupName(rawName, index, contextLabel) {
  const trimmed = (rawName || '').toString().trim();
  const context = (contextLabel || DEFAULT_SUBGROUP_LABEL);
  if (!trimmed) return buildGroupName(index + 1, context);
  const legacyNumeric = /^ក្រុម\s*[\d\u17E0-\u17E9]+$/;
  const legacyOrdinal = /^ក្រុមទី[\d\u17E0-\u17E9]+$/;
  const legacyAlpha = /^ក្រុម\s*[A-Z]$/i;
  if (legacyNumeric.test(trimmed) || legacyAlpha.test(trimmed) || legacyOrdinal.test(trimmed)) return buildGroupName(index + 1, context);
  return trimmed;
}
function isAutoGeneratedGroupName(name) {
  const trimmed = (name || '').toString().trim();
  if (!trimmed) return true;
  return /^ក្រុម.*ទី[\d\u17E0-\u17E9]+$/.test(trimmed);
}

// Parse a time range from a shift title like "7:30AM-3:30PM" or "07:30-15:30".
// Returns { start: 'HH:MM', end: 'HH:MM' } or null when not parseable.
function parseTimeRangeFromTitle(title) {
  if (!title) return null;
  try {
    const txt = title.toString().trim();
    // Match patterns like '7:30AM-3:30PM'
    const m = txt.match(/(\d{1,2}:\d{2}\s*(?:AM|PM|am|pm)?)\s*[-–]\s*(\d{1,2}:\d{2}\s*(?:AM|PM|am|pm)?)/);
    if (!m) return null;
    const norm = (t) => {
      let s = t.toString().trim();
      const ampm = s.match(/(AM|PM|am|pm)$/);
      if (ampm) {
        s = s.replace(/(AM|PM|am|pm)$/i, '').trim();
        const [hStr, mStr] = s.split(':');
        let h = parseInt(hStr, 10);
        const mm = String((mStr || '00')).padStart(2, '0');
        if (/pm/i.test(ampm[0]) && h < 12) h += 12;
        if (/am/i.test(ampm[0]) && h === 12) h = 0;
        return `${String(h).padStart(2, '0')}:${mm}`;
      }
      // assume already 24-hour HH:MM
      const parts = s.split(':');
      const hh = String(parseInt(parts[0] || '0', 10)).padStart(2, '0');
      const mm = String((parts[1] || '00')).padStart(2, '0');
      return `${hh}:${mm}`;
    };
    return { start: norm(m[1]), end: norm(m[2]) };
  } catch (e) {
    return null;
  }
}

