  // Compute the intended category for each subgroup based on name/department and category labels
  function computeAutoDistributedSubgroups(prevMap) {
    // Auto-distribute subgroups by matching their name or dept to category labels.
    // If no category label matches, keep the subgroup in its original category.
    const order = ensureCategoryOrder(categoryOrder);
    const next = createEmptyCategoryMap(order);
    const norm = (s) => (s || '').toString().replace(/\u200B/g, '').trim().toLowerCase();
    const labelMap = order.reduce((acc, id) => { acc[id] = norm(getCategoryLabel(id)); return acc; }, {});

    // Walk every category's groups and place them into the best-matching category
    order.forEach(fromId => {
      (prevMap[fromId] || []).forEach(g => {
        // If a subgroup was explicitly generated/created (manual flag),
        // don't auto-distribute it — keep it in its source category.
        if (g && g.manual) {
          next[fromId] = [...(next[fromId] || []), { ...g, categoryId: fromId }];
          return; // continue to next subgroup
        }
        const nameN = norm(g.name);
        const deptN = norm(g.dept);
        // default target is the group's current categoryId if valid, otherwise its source
        let target = (g.categoryId && order.includes(g.categoryId)) ? g.categoryId : fromId;
        for (const cid of order) {
          const lbl = labelMap[cid];
          if (!lbl) continue;
          // If the group's name or dept text contains the category label, assign it there
          if ((nameN && nameN.includes(lbl)) || (deptN && deptN.includes(lbl))) { target = cid; break; }
        }
        next[target] = [...(next[target] || []), { ...g, categoryId: target }];
      });
    });
    return next;
  }

  function categoryMapsEqual(a, b) {
    const aKeys = Object.keys(a || {});
    const bKeys = Object.keys(b || {});
    if (aKeys.length !== bKeys.length) return false;
    for (const k of aKeys) {
      const ag = a[k] || [];
      const bg = b[k] || [];
      if (ag.length !== bg.length) return false;
      for (let i = 0; i < ag.length; i++) {
        if (ag[i]?.id !== bg[i]?.id) return false;
        if (ag[i]?.categoryId !== bg[i]?.categoryId) return false;
      }
    }
    return true;
  }

